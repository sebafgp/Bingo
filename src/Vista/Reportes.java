    private void botonReporteJuegosActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonReporteJuegosActionPerformed
        // TODO add your handling code here:
        String nombreBingo = JOptionPane.showInputDialog(rootPane, "Ingrese nombre del bingo");
        if (nombreBingo != null && !nombreBingo.isEmpty()){
            if (c.estaBingo(nombreBingo)){
                try {
                    c.reporteJuegos(nombreBingo);
                    JOptionPane.showMessageDialog(rootPane, "Se ha creado el reporte para el bingo " + nombreBingo);
                } catch (FileNotFoundException ex) {
                    Logger.getLogger(Reportes.class.getName()).log(Level.SEVERE, null, ex);
                }
            }
            else{
                JOptionPane.showMessageDialog(rootPane, "No se ha encontrado ese bingo");
            }
        }
    }//GEN-LAST:event_botonReporteJuegosActionPerformed

